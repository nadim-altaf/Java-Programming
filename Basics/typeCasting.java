public class typeCasting {
  public static void main(String[] args) {
    int n = 6;
    float b = (float)n;
    System.out.println(b);

    char ch = 'A';
    int i = (int)ch;
    System.out.println(i);

    byte bt = 123;
    int j = (int)bt;
    System.out.println(j);

    short s = 12345;
    int t = (int)s;
    System.out.println(t);

    int p = 1234567890;
    double d = (double)p;
    System.out.println(d);

    // boolean bl = true;
    // int t = (int)bl;
    // System.out.println(t);   SHOWS ERROR


  }
  
}
